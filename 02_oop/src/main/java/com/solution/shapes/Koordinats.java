package com.solution.shapes;

/**
 * The class creates the coordinates of the point
 */

public class Koordinats {

    private double x;

    private double y;

    public Koordinats(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
